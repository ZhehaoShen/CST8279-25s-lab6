import time
from adafruit_circuitplayground import cp

def CelToFahr(cel):
    fahr = (cel * 1.8) + 32
    return fahr

def FahrToCel(fahr):
    cel = (fahr - 32) / 1.8
    return cel

while True:
    temp_celsius = cp.temperature
    # Switch is True
    if cp.switch:
        temp_fahrenheit = CelToFahr(temp_celsius)
        print(f"Temperature: {temp_celsius}째C -> {temp_fahrenheit}째F")
    # Switch is False
    else:
        temp_fahrenheit = CelToFahr(temp_celsius)
        temp_converted_celsius = FahrToCel(temp_fahrenheit)
        print(f"Temperature: {temp_fahrenheit}째F -> {temp_converted_celsius}째C")
    
    time.sleep(1)